// ─── Lookup helpers ───────────────────────────────────────────────────────────

/// Find an OH image card by number (1–44).
pub fn oh_image_by_number(n: u8) -> Option<&'static OhImageCard> {
    OH_IMAGES.iter().find(|c| c.number == n)
}

/// Find an OH word card by number (1–44).
pub fn oh_word_by_number(n: u8) -> Option<&'static OhWordCard> {
    OH_WORDS.iter().find(|c| c.number == n)
}

/// Find an OH word card by word (case-insensitive, partial match).
#[allow(dead_code)]
pub fn oh_word_by_word(query: &str) -> Option<&'static OhWordCard> {
    let q = query.to_lowercase();
    OH_WORDS
        .iter()
        .find(|c| c.word.to_lowercase().contains(&q))
}
