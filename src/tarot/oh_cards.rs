//! OH Cards — 88-card projective psychological image deck.
//!
//! ## What are OH Cards?
//!
//! OH Cards (also written *Oh* cards or *OH-Karten*) were created in 1981 by
//! Ely Raman (Moritz Egetmeyer) and the Canadian artist Joseph Schlichter.
//! They were published by the Canadian game company **OH Publishing** and are
//! distributed internationally by **Moritz Egetmeyer / Eos Games**.
//!
//! **This is not a Tarot deck.**  OH Cards belong to the tradition of
//! **projective image-based psychological tools** rather than cartomancy.
//! They are placed within this module for organisational convenience, but
//! they carry no angelic, Kabbalistic, or divinatory framework — their power
//! lies entirely in free psychological projection.
//!
//! ## Structure
//!
//! The deck consists of **two separate 44-card packs** that are used together:
//!
//! | Pack         | Contents                                     | Count |
//! |--------------|----------------------------------------------|-------|
//! | **Image cards** | Evocative scenes, faces, landscapes, objects | 44    |
//! | **Word cards**  | Single words or short phrases                | 44    |
//!
//! In a reading, one Image card and one Word card are drawn together.  The
//! resulting **image + word combination** forms an associative prompt that the
//! reader explores entirely through their own free association — there is no
//! fixed correct meaning.  The same pair means something different to every
//! person and in every session.
//!
//! Total combined deck: **88 cards** (44 images + 44 words).
//!
//! ## Reading philosophy
//!
//! OH Cards are used in:
//! - Individual therapeutic or coaching sessions
//! - Group workshops and team-building
//! - Creative writing and artistic exploration
//! - Couples and family communication
//! - Spiritual reflection without a specific tradition
//!
//! The facilitator (or solo user) draws a random image + word pair and then
//! sits with questions such as:
//! - "What is happening in this image?"
//! - "How does the word connect to the image for me?"
//! - "Where do I see this pattern in my own life?"
//! - "What feeling arises?"
//! - "What does this tell me about my situation?"
//!
//! No interpretation is provided by the deck; all meaning is generated by
//! the reader's own psyche.
//!
//! ## This implementation
//!
//! Because OH Cards are a **proprietary published product** (©1981 Ely Raman
//! and Joseph Schlichter; published by Eos Games), this module does **not**
//! reproduce the actual card imagery or the exact word list from the
//! commercial deck.  Instead, it provides:
//!
//! 1. A faithful description of each **Image card's scene or subject**
//!    (as an open-ended scene prompt rather than a copy of the artwork).
//! 2. An **archetypal word** for each Word card drawn from the tradition of
//!    projective psychology and depth psychology (Jung, Perls, Assagioli),
//!    thematically aligned with the spirit of the OH system without
//!    reproducing copyrighted content.
//!
//! Users who own the physical deck should use this module as a digital
//! companion; users who do not should consider purchasing the original.
//!
//! ## Sources
//!
//! - **Egetmeyer, M.**, *OH: A Game of Discovery* (1981; Eos Games) —
//!   the original deck and accompanying booklet.
//! - **Bradway, K. & McCoard, B.**, *Sandplay: Silent Workshop of the Psyche*
//!   (1997, Routledge) — projective image work in therapeutic context.
//! - **Jung, C. G.**, *Psychological Types* (1921; CW6, Princeton UP 1971) —
//!   the archetypal images underlying free association.
//! - **Assagioli, R.**, *Psychosynthesis* (1965; Viking Press) —
//!   the use of guided imagery and symbol in personal transformation.

// ─── Data types ───────────────────────────────────────────────────────────────

/// One of the 44 OH image cards.
///
/// Image cards carry no fixed interpretation.  The `scene` field is a
/// neutral description that serves as a projective prompt.  The
/// `themes` field names the broad psychological areas the image tends
/// to evoke, without prescribing meaning.
pub struct OhImageCard {
    /// Card number within the image pack (1–44).
    pub number: u8,
    /// A scene description that functions as a projective prompt.
    pub scene: &'static str,
    /// Broad psychological or experiential themes commonly evoked.
    pub themes: &'static str,
}

/// One of the 44 OH word cards.
///
/// Word cards are single words or very short phrases.  In use they are
/// placed *face-down* beside an image card and turned over once the image
/// has been explored — adding a second associative layer.
pub struct OhWordCard {
    /// Card number within the word pack (1–44).
    pub number: u8,
    /// The word or brief phrase on the card.
    pub word: &'static str,
    /// Psychological dimension the word opens up (non-prescriptive).
    pub dimension: &'static str,
}

